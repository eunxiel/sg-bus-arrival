const SETTINGS_KEY = "sgbus:settings";

/**
 * Non-reactive language lookup for contexts that can't use the useTranslation
 * hook (e.g. the ErrorBoundary class component's default fallback text).
 */
export function getStoredLanguage(): AppLanguage {
  if (typeof window === "undefined") return "en";
  try {
    const raw = window.localStorage.getItem(SETTINGS_KEY);
    if (raw) {
      const parsed = JSON.parse(raw);
      if (parsed?.language && parsed.language in translations) {
        return parsed.language as AppLanguage;
      }
    }
  } catch {
    // ignore malformed storage
  }
  return "en";
}

export function translate(
  lang: AppLanguage,
  key: string,
  params?: Record<string, string | number>
): string {
  const dict = translations[lang] ?? translations.en;
  let str = dict[key] ?? translations.en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return str;
}
